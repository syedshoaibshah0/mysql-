DELIMITER //
CREATE PROCEDURE INSERTDATA(STDNAME VARCHAR(250), CRSE VARCHAR(250))
BEGIN
  INSERT INTO STUDENT (NAME, COURSE) VALUES (STDNAME, CRSE);
END
