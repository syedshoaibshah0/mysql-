CREATE PROCEDURE UPDATEDATA(STDNAME VARCHAR(250), CRSE VARCHAR(250), STDNTNAME VARCHAR(250))
BEGIN
  UPDATE STUDENT SET NAME = STDNAME, COURSE = CRSE WHERE NAME = STDNTNAME;
END
